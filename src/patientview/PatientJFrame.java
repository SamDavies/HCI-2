    public PatientJFrame() throws IOException {
        initComponents();
        //set window in the center of the screen
        //Get the size of the screen
        Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
        //Determine the new location of the window
        int w = this.getSize().width;
        int h = this.getSize().height;
        int x = (dim.width - w) / 2;
        int y = (dim.height - h) / 2;
        //Move the window
        this.setLocation(x, y);

        // set the initial patient
        this.setPatientFields();

        //set focus on exit button
        jButton_exit.requestFocus();
        //set timer
        timerSeconds = 0;
        if (timer == null) {
            timer = new Timer(500, new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    // do it every 1 second
                    Calendar cal = Calendar.getInstance();
                    cal.getTime();
                    SimpleDateFormat sdf = new SimpleDateFormat("YYYY-MM-dd HH:mm:ss");
                    jLabel_systemTime.setText(sdf.format(cal.getTime()));
                    // do it every 5 seconds
                    if (timerSeconds % 5 == 0) {
                        String key = Integer.toString((timerSeconds % 3600) + 5);
                        displayData(model.getSelectedPatient().data.get(key));
                    }
                    timerSeconds++;
                }
            });
        }

        if (!timer.isRunning()) {
            timer.start();
        }
    }

    public void setPatientFields() {
        Patient patient = model.getSelectedPatient();

        patientNameField.setText(patient.getFistName() + " " + patient.getLastName());
        wardNumField.setText("W001");
        bedNumField.setText(Integer.toString(patient.getBedNumber()));
        dobField.setText(patient.getDOB());
        genderField.setText(patient.getGender());
    }

    private void displayData(PatientData patientData) {
        // breathing
        breathingField.setText(Integer.toString(patientData.getBreathing()));
        Color red = new Color(250, 85, 65);
        this.breathingBar.setBarColour(red);
        this.breathingBar.setPercentage((int) (100*patientData.getBreathing()/45.0));

        // oxygen
        oxygenField.setText(Integer.toString(patientData.getOxygen()));
        Color blue = new Color(65, 175, 250);
        this.oxygenBar.setBarColour(blue);
        this.oxygenBar.setPercentage((int) (100*patientData.getOxygen()/105.0));

        // temperature
        temperatureField.setText(String.format("%.02f", patientData.getTemperature()));
        Color yellow = new Color(250, 175, 65);
        this.temperatureBar.setBarColour(yellow);
        this.temperatureBar.setPercentage((int) (100*patientData.getTemperature()/45.0));

        // blood
        bloodPressureField.setText(Integer.toString(patientData.getBloodPressure()));
        Color green = new Color(175, 250, 65);
        this.bloodBar.setBarColour(green);
        this.bloodBar.setPercentage((int) (100*patientData.getBloodPressure()/210.0));

        // heart
        heartRateField.setText(Integer.toString(patientData.getHeartRate()));
        Color orange = new Color(175, 65, 250);
        this.heartBar.setBarColour(orange);
        this.heartBar.setPercentage((int) (100*patientData.getHeartRate()/170.0));

        this.repaint();
    }

